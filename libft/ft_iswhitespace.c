#include "libft.h"

int	ft_iswhitespace(int c)
{
	if (c == ' ' || c == '\t' || c == '\n' || c == '\v' || c == '\f'
		|| c == '\r')
	{
		ft_putchar('A');
		return (-1);
	}
	return (0);
}
